//FLASH写入
void writeFlash(void)
{
	uint32_t writeFlashData ;
	uint32_t addr = 0x0800C000;
	
	writeFlashData=(((uint32_t)FrequencyEncoder)<<16) | FrequencyTorque;
	//1、解锁FLASH
  HAL_FLASH_Unlock();
	
	//2、擦除FLASH
	//初始化FLASH_EraseInitTypeDef
	FLASH_EraseInitTypeDef f;
	f.Banks=0; 
	f.NbSectors=1;
	f.Sector=FLASH_SECTOR_3;
	f.TypeErase=FLASH_TYPEERASE_SECTORS;
	f.VoltageRange=FLASH_VOLTAGE_RANGE_3;
	//设置PageError
	uint32_t SectorError = 0;
	//调用擦除函数
	HAL_FLASHEx_Erase(&f, &SectorError);

	//3、对FLASH烧写
	HAL_FLASH_Program(TYPEPROGRAM_WORD, addr, writeFlashData);
	
	//4、锁住FLASH
  HAL_FLASH_Lock();
}

