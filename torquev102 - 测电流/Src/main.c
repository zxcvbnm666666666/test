int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_USB_OTG_FS_PCD_Init(); 
  MX_TIM4_Init();
  MX_TIM8_Init();
  MX_TIM6_Init();
  MX_TIM7_Init();
  MX_USART3_UART_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM5_Init();
  MX_IWDG_Init();

  /* USER CODE BEGIN 2 */
	ENCODER_PWR_ON;
	TORQUE_PWR_ON;
	H6PWR_EN_ON;
	
	//readFlash();
	
	//重设tim6、tim7周期
	if (FrequencyEncoder>0 && FrequencyEncoder<=1000)
		htim6.Init.Period = (10000/FrequencyEncoder)-1;
	else
		htim6.Init.Period = (10000/1)-1;
  if (HAL_TIM_Base_Init(&htim6) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }
	
	if (FrequencyTorque>0 && FrequencyTorque<=1000)
		htim7.Init.Period = (10000/FrequencyTorque)-1;
	else 
		htim7.Init.Period = (10000/100)-1;
  if (HAL_TIM_Base_Init(&htim7) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }
	
	__HAL_TIM_SET_COUNTER(&htim6,0);
	HAL_TIM_Base_Start_IT(&htim6);
	__HAL_TIM_SET_COUNTER(&htim7,0);
	HAL_TIM_Base_Start_IT(&htim7);
	
	__HAL_TIM_SET_COUNTER(&htim1,0);
	HAL_TIM_Encoder_Start(&htim1, TIM_CHANNEL_1); //打开ch7编码器
	__HAL_TIM_SET_COUNTER(&htim2,0);
	HAL_TIM_Encoder_Start(&htim2, TIM_CHANNEL_1); //打开ch8编码器
	__HAL_TIM_SET_COUNTER(&htim3,0);
	HAL_TIM_Encoder_Start(&htim3, TIM_CHANNEL_1); //打开ch4编码器
	__HAL_TIM_SET_COUNTER(&htim4,0);
	HAL_TIM_Encoder_Start(&htim4, TIM_CHANNEL_1); //打开ch7编码器
	__HAL_TIM_SET_COUNTER(&htim5,0);
	HAL_TIM_Encoder_Start(&htim5, TIM_CHANNEL_1); //打开ch8编码器
	__HAL_TIM_SET_COUNTER(&htim8,0);
	HAL_TIM_Encoder_Start(&htim8, TIM_CHANNEL_1); //打开ch4编码器
	
	__HAL_UART_ENABLE_IT(&huart3, UART_IT_RXNE); 
	__HAL_UART_ENABLE_IT(&huart3, UART_IT_IDLE);
	
	//开看门狗
	HAL_IWDG_Refresh(&hiwdg);
	
	LED1_OFF;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
		//刷新看门狗
		HAL_IWDG_Refresh(&hiwdg);
		
		if (Received_USART3==1){
			if (RsPoint_USART3>=8){
				if (RsBuf_USART3[RsPoint_USART3-8]==0x55 && RsBuf_USART3[RsPoint_USART3-7]==0xAA){
					
					CrcCode=Get_Crc16(&RsBuf_USART3[RsPoint_USART3-6],4);
					if (CrcCode==(((uint16_t )RsBuf_USART3[RsPoint_USART3-2])<<8|RsBuf_USART3[RsPoint_USART3-1])){
						unsigned char SendMsg[8];
						unsigned char i;
						
						FrequencyEncoder=((uint16_t)RsBuf_USART3[RsPoint_USART3-6])<<8 | RsBuf_USART3[RsPoint_USART3-5];
						FrequencyTorque=((uint16_t)RsBuf_USART3[RsPoint_USART3-4])<<8 | RsBuf_USART3[RsPoint_USART3-3];
						
						writeFlash();
						
						//重设tim6、tim7周期
						if (FrequencyEncoder>0 && FrequencyEncoder<=1000)
							htim6.Init.Period = (10000/FrequencyEncoder)-1;
						else
							htim6.Init.Period = (10000/1)-1;
						if (HAL_TIM_Base_Init(&htim6) != HAL_OK)
						{
							_Error_Handler(__FILE__, __LINE__);
						}
						
						if (FrequencyTorque>0 && FrequencyTorque<=1000)
							htim7.Init.Period = (10000/FrequencyTorque)-1;
						else 
							htim7.Init.Period = (10000/100)-1;
						if (HAL_TIM_Base_Init(&htim7) != HAL_OK)
						{
							_Error_Handler(__FILE__, __LINE__);
						}
						
						for (i=0;i<8;i++){
							SendMsg[i]=RsBuf_USART3[RsPoint_USART3-8+i];
						}
						USART3_Send(SendMsg,8);
					}
				}
			}
			//清接收标记
			Received_USART3=0;
			RsPoint_USART3=0;
		}
		
  }
  /* USER CODE END 3 */

}

/** System Clock Configuration
*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;

    /**Configure the main internal regulator output voltage 
    */
  __HAL_RCC_PWR_CLK_ENABLE();

  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE1);

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_LSI|RCC_OSCILLATORTYPE_HSE;
  RCC_OscInitStruct.HSEState = RCC_HSE_ON;
  RCC_OscInitStruct.LSIState = RCC_LSI_ON;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
  RCC_OscInitStruct.PLL.PLLM = 4;
  RCC_OscInitStruct.PLL.PLLN = 72;
  RCC_OscInitStruct.PLL.PLLP = RCC_PLLP_DIV2;
  RCC_OscInitStruct.PLL.PLLQ = 3;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV2;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_2) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

